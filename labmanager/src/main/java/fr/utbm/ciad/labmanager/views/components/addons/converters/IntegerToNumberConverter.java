package fr.utbm.ciad.labmanager.views.components.addons.converters;

import com.vaadin.flow.data.binder.Result;
import com.vaadin.flow.data.binder.ValueContext;
import com.vaadin.flow.data.converter.Converter;

/**
 * A converter from int to Number.
 *
 * @author $Author: sgalland$
 * @version $Name$ $Revision$ $Date$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 * @since 4.0
 */
public class IntegerToNumberConverter implements Converter<Integer, Number> {

    private static final long serialVersionUID = -554020906295134581L;

    @Override
    public Result<Number> convertToModel(Integer value, ValueContext context) {
        if (value == null) {
            return Result.ok(Integer.valueOf(0));
        }
        return Result.ok(value);
    }

    @Override
    public Integer convertToPresentation(Number value, ValueContext context) {
        if (value == null) {
            return Integer.valueOf(0);
        }
        return Integer.valueOf(value.intValue());
    }

}
